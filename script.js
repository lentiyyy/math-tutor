// Тема: применяем сохранённую раньше всего, чтобы не было «мигания»
const applyStoredTheme = () => {
    if (localStorage.getItem('theme') === 'dark') {
        document.addEventListener('DOMContentLoaded', () => {
            document.body.classList.add('dark');
            const input = document.querySelector('.switch-input');
            if (input) input.checked = true;
        });
    }
};
applyStoredTheme();

const initMobileMenu = () => {
    const trigger = document.querySelector('.menu-toggle');
    const menu = document.querySelector('.menu');
    if (trigger && menu) {
        trigger.addEventListener('click', () => menu.classList.toggle('is-open'));
    }
};

const initThemeSwitch = () => {
    const themeInput = document.querySelector('.switch-input');
    if (themeInput) {
        themeInput.addEventListener('change', () => {
            const isDark = themeInput.checked;
            document.body.classList.toggle('dark', isDark);
            localStorage.setItem('theme', isDark ? 'dark' : 'light');
        });
    }
};

const initShowMoreReviews = () => {
    const showMoreBtn = document.getElementById('showMoreReviews');
    const reviewsGrid = document.getElementById('reviewsGrid');
    if (showMoreBtn && reviewsGrid) {
        showMoreBtn.addEventListener('click', () => {
            renderAllReviews(reviewsGrid);
            showMoreBtn.parentElement.style.display = 'none';
        });
    }
};

document.addEventListener('DOMContentLoaded', () => {
    initMobileMenu();
    initThemeSwitch();
    initShowMoreReviews();
});

// Сабмит формы записи (доступен из inline onsubmit)
window.handleForm = (event) => {
    event.preventDefault();
    const status = document.getElementById('formStatus');
    if (status) {
        status.textContent = '✓ Заявка отправлена! Свяжусь с вами в течение часа.';
        event.target.reset();
        setTimeout(() => { status.textContent = ''; }, 6000);
    }
    return false;
};

