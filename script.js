const renderAllReviews = (grid) => {
    const html = additionalReviews.map((r, i) => {
        const [name, role, stars, text] = r;
        const colorIndex = (i % 6) + 1;
        const letter = name.trim().charAt(0).toUpperCase();
        const starsLine = '★ '.repeat(stars).trim();
        return `
            <article class="review-card">
                <div class="review-card-head">
                    <div class="review-pic review-pic-${colorIndex}">${letter}</div>
                    <div>
                        <strong>${name}</strong>
                        <span>${role}</span>
                    </div>
                </div>
                <div class="review-stars">${starsLine}</div>
                <p>«${text}»</p>
            </article>
        `;
    }).join('');
    grid.insertAdjacentHTML('beforeend', html);
};
